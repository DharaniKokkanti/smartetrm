import { chromium } from 'playwright';
const b = await chromium.launch();
const page = await b.newPage();
await page.goto('http://localhost:5183');
await page.locator('text=Sign in as dev.admin').click().catch(() => {});
await page.waitForTimeout(1500);
const r1 = await page.evaluate(() => fetch('/api/v1/trade-custom-field-values?tradeId=1').then(r => r.json()));
console.log('trade values:', JSON.stringify(r1));
const r2 = await page.evaluate(() => fetch('/api/v1/trade-order-custom-field-values?orderId=1').then(r => r.json()));
console.log('leg values:', JSON.stringify(r2));
const r3 = await page.evaluate(() => fetch('/api/v1/custom-field-definitions').then(r => r.json()));
console.log('definitions:', JSON.stringify(r3));
await b.close();
